package Core;

public class StrBuff {

	public static void main(String[] args) {
			
		StringBuffer sb = new StringBuffer("karthik");
		sb.append("dheena");
		
		System.out.println(sb);
													//mutable string
		
		StringBuilder sd = new StringBuilder("Karthik");
		sd.append("D");
		
		System.out.println(sd);

	}

}
